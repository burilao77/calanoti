    /**
     * Index method
     *
     * @return \Cake\Http\Response|void
     */
    public function index()
    {
     
        $time = Time::now();
        $date_actual = $time;
  
        $orders = TableRegistry::get('Orders');
        $query = $orders->find()
                                ->select(['id', 'name', 'price', 'description'])
                                ->where(['date_expiration <' => $date_actual]);
       $this->set('orders', $this->paginate($query));
    }

